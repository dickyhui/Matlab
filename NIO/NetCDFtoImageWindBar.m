function [ ] = NetCDFtoImageWindBar(ncid,variables,dimLon,dimLat,levels,times,piclevels,picinterval)
global Time_interval Level_interval Date OutputDirectory VectorPicSize DataType Time_Start
%  生成NetCDF的风向标
%  ncid：NetCDF的ID
%  variables:风向标的数据参数
%  start:nc文件数据的起点位置
%  count:向后计数 二维为经度和纬度，三维加层数或时间
%  levels:该参数数据的层数
%  times:该参数数据的时间数
%  piclevels：生成图片的级别（3,4,5,6）
%  picinterval：根据图片的不同级别设置数据间隔（32,16,8,4）
%  Time_interval：数据时间间隔
%  Level_interval：数据层数间隔
%  Date：数据时间
%  OutputDirectory：输出文件夹路径
%  VectorPicSize：输出图片大小，大小对应于piclevels的最小级别的图片
%  DataType:数据类型（Wind/Wave/..）
%  MFILE:   NetCDFtoImageWindBar.m
%  MATLAB:  7.13.0.564 (R2011b)
%  VERSION: 1.0 (2013-3-27)
%  AUTHOR:  LinXianhui
%  CONTACT: linxianhui_zju@163.com
%  MODIFY:
%  DATE:

variable=variables;
vidU=netcdf.inqVarID(ncid,variable{1}); % 获取U变量名的ID
vidV=netcdf.inqVarID(ncid,variable{2}); % 获取V变量名的ID
%ecU=netcdf.getVar(ncid,vidU,start,count); % 读取U变量
%ecV=netcdf.getVar(ncid,vidV,start,count); % 读取V变量
try    
levelval = netcdf.getAtt(ncid,vidU, 'level_hpa');
    m = 1;
    for n=1:length(levelval)       
        if(levelval(n) == 850 || levelval(n) == 500 || levelval(n) == 200)
            levelist(m) = n-1;
            m = m + 1;
        end
    end
catch me
end
l=1;
%循环级别
for level=piclevels
    %根据不同的级别(level)设置出图的大小，呈指数递增
    set(gcf,'paperposition',[0 0 2^(l-1)*VectorPicSize(1)/get(0,'ScreenPixelsPerInch') 2^(l-1)*VectorPicSize(2)/get(0,'ScreenPixelsPerInch')]);
    i=0;j=Time_Start;p=0;
    
    if(levels==1&&times~=1)   %三维，经度纬度时间
        while j<times
            %出图的图层路径（包括文件名）
            fullPath = [OutputDirectory,Date,'\',DataType,'-', variable{1},variable{2},'-',num2str(j) ,'-',num2str(i) ,'-',Date , '-L',num2str(level), '.png'];
            ecU=netcdf.getVar(ncid,vidU,[0,0,j],[dimLon,dimLat,1]); % 读取U变量
            ecV=netcdf.getVar(ncid,vidV,[0,0,j],[dimLon,dimLat,1]); % 读取V变量
            DrawWindBar(flipud(ecU(:,:,1)'),flipud(ecV(:,:,1)'),fullPath,picinterval(l));
            j=j+Time_interval;
        end
    elseif(levels~=1&&times==1) %三维，经度纬度层数
        while i<levels
            fullPath = [OutputDirectory,Date,'\',DataType,'-', variable{1},variable{2},'-',num2str(j) ,'-',num2str(i) ,'-',Date , '-L',num2str(level), '.png'];
            ecU=netcdf.getVar(ncid,vidU,[0,0,i],[dimLon,dimLat,1]); % 读取U变量
            ecV=netcdf.getVar(ncid,vidV,[0,0,i],[dimLon,dimLat,1]); % 读取V变量
            DrawWindBar(flipud(ecU(:,:,1)'),flipud(ecV(:,:,1)'),fullPath,picinterval(l));
            i=i+Level_interval;
        end
    else   
        %循环层数
        for i=levelist
            %循环时间
            while j<times
                %出图的图层路径（包括文件名）
                fullPath = [OutputDirectory,Date,'\',DataType,'-', variable{1},variable{2},'-',num2str(j) ,'-',num2str(p) ,'-',Date , '-L',num2str(level), '.png'];            
                %画风向标(矩阵逆时针旋转90°)
                ecU=netcdf.getVar(ncid,vidU,[0,0,i,j],[dimLon,dimLat,1,1]); % 读取U变量
                ecV=netcdf.getVar(ncid,vidV,[0,0,i,j],[dimLon,dimLat,1,1]); % 读取V变量
                DrawWindBar(flipud(ecU(:,:,1,1)'),flipud(ecV(:,:,1,1)'),fullPath,picinterval(l));           
                j=j+Time_interval;
            end
            j=Time_Start;
            p = p + 1;
        end
    end
    l=l+1;
end

clear ecU ecV;
end

